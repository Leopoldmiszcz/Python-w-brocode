name = "Leopold"

print(len(name)) #How long the string is
print(name.find("L")) #Shows us where the character is in the string
print(name.capitalize()) #Capitalizes only the first letter
print(name.upper()) #Uppercases the whole name
print(name.lower()) #Lowercases the whole name
print(name.isdigit()) #Checks if that variable is a digit
print(name.isalpha()) #Checks if those are alphabetical characters. If there's a space them it will be false
print(name.count("o")) #Counts how many specified letters are in the variable. In this example letter "o"
print(name.replace("o", "a")) #Replaces specified letter in the variable. In this example "o" to "a"
print(name*3) #Displays string specified number of times. In this example for 3 times

