import math

pi = 3.14

x = 1
y = 2
z = 3

print(round(pi))
print(math.ceil(pi)) #rounds variable up
print(math.floor(pi)) #rounds variable down
print(abs(pi)) #gives absolute value of variable (how far that number is from zero)
print(pow(pi, 2)) #powers to the two
print(math.sqrt(42)) #square roots a number
print(max(x, y, z)) #findes the largest value
print(min(x, y, z)) #findes the lowest value